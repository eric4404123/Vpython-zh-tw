####################################程式開始###################################
from visual import *

#------------------------------------------------------------------------------
#  1. 參數設定
#------------------------------------------------------------------------------
v = 0.03        #木塊速度 = 0.03 m/s
dt = 0.001      #畫面更新的時間間隔，單位為s
t = 0          #模擬所經過的時間 ，單位為s，初始值為0

#------------------------------------------------------------------------------
#  2. 畫面設定
#------------------------------------------------------------------------------
scene = 顯示(title='1', width=800, height=800, x座標=0, y=0, center=(0,0.06,0), background=(0.5,0.6,0.5))
floor = 箱體(pos=(0,-(0.005)/2,0), 長度=0.3, 高度=0.005, 寬度=0.1)
cube = 箱體(pos=(0, 0.05/2, 0), 長度=0.05, 高度=0.05, 寬度=0.05)

#------------------------------------------------------------------------------
#  3. 物體運動部分
#------------------------------------------------------------------------------
while(cube.位置.x座標 < 0.10):
    rate(1000)   
    t += dt
    cube.位置.x座標 += v*dt
